let dia = "";
let diaSemana = 3;
switch (diaSemana){
    case 1:
        dia = "segunda";
        break
    case 2:
        dia = "terça";
        break
    case 3:
        dia = "quarta"
        break
    case 4:
        dia = "quinta"
        break
    case 5:
        dia = "sexta"
        break
    case 6:
        dia = "sabado"
    case 7:
        dia = "domingo"
        break
    default:
        dia = "mês inexistente"
}
console.log(dia)
